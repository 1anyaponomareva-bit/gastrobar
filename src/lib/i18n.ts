const LANGS: AppLang[] = ["ru", "en", "vn"];

export function nextAppLang(current: AppLang): AppLang {
  const i = LANGS.indexOf(current);
  return LANGS[i === -1 || i === LANGS.length - 1 ? 0 : i + 1] ?? "ru";
}

/** Fallback: текущий язык → ru → key */
export function translate(lang: AppLang, key: string): string {
  const r = translations[lang] ?? translations.ru;
  const ru = translations.ru;
  return (r[key] ?? ru[key] ?? key).trim() || key;
}
