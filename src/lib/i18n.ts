export type AppLang = "ru" | "en" | "vn";

const VALID_LANGS: AppLang[] = ["ru", "en", "vn"];

const STORAGE_KEY_LANG = "lang";
/** Старый ключ сплэша / внешних скриптов (до унификации). */
const STORAGE_KEY_LANG_LEGACY = "gastrobar-lang";

function readRawLangFromLocalStorage(): string | null {
  if (typeof window === "undefined") return null;
  try {
    const primary = localStorage.getItem(STORAGE_KEY_LANG);
    if (primary && primary.trim()) return primary;
    return localStorage.getItem(STORAGE_KEY_LANG_LEGACY);
  } catch {
    return null;
  }
}

/**
 * Браузер/сплэш иногда пишут `vi` для вьетнамского; в приложении канон — `vn`.
 */
export function normalizeAppLangString(raw: string | null | undefined): AppLang {
  if (raw == null) return "ru";
  const s = String(raw).trim().toLowerCase();
  if (s === "vi") return "vn";
  if ((VALID_LANGS as string[]).includes(s)) return s as AppLang;
  return "ru";
}

export function writeStoredAppLang(lang: AppLang): void {
  if (typeof window === "undefined") return;
  try {
    localStorage.setItem(STORAGE_KEY_LANG, lang);
  } catch {
    /* ignore */
  }
}

/** Для `formatError*`, `userFacing*`, классов error boundary вне React. */
export function readStoredAppLang(): AppLang {
  if (typeof window === "undefined") return "ru";
  return normalizeAppLangString(readRawLangFromLocalStorage());
}

